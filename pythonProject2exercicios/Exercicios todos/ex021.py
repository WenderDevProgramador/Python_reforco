import math
ang=float(input('Qual o angulo ? '))
rad=math.radians(ang)
seno=math.sin(rad)
cosseno=math.cos(rad)
tangente=math.tan(rad)
print('O angulo digitado foi {} e o seu seno é {:.2f} cosseno {:.2f} e tangente {:.2f} '.format(ang,seno,cosseno,tangente))

